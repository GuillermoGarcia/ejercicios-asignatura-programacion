/**
 * Muestra por pantalla diez palabras en ingles y su traducción en formato de dos columnas
 * Ejercicio 3 Tema 1
 * @author Guillermo Garcia 
**/

public class Ejercicio03 { // Clase principal
  public static void main(String[] args) {
    System.out.println("  Palabra       Traducción");
    System.out.println("-----------------------------------------");
    System.out.printf("%-15s   %-16s\n","Name","Nombre");
    System.out.printf("%-15s   %-16s\n","Home","Casa");
    System.out.printf("%-15s   %-16s\n","Gift","Regalo");
    System.out.printf("%-15s   %-16s\n","Garden","Jardin");
    System.out.printf("%-15s   %-16s\n","Whale","Ballena");
    System.out.printf("%-15s   %-16s\n","Shop","Tienda");
    System.out.printf("%-15s   %-16s\n","Office","Oficina");
    System.out.printf("%-15s   %-16s\n","Message","Mensaje");
    System.out.printf("%-15s   %-16s\n","Find","Buscar");
    System.out.printf("%-15s   %-16s\n","Help","Ayuda");
  }
}
